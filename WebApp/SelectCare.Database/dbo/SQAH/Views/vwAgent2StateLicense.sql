CREATE VIEW dbo.vwAgent2StateLicense
AS
SELECT     TOP (100) PERCENT dbo.gal_states.state_id, dbo.state_licensure.stl_usr_key, dbo.gal_states.state_id AS Expr1, dbo.states.sta_key, dbo.gal_states.state_name, 
                      dbo.gal_states.state_code, dbo.gal_states.state_inactive, dbo.gal_states.state_tz_id
FROM         dbo.gal_states INNER JOIN
                      dbo.states ON dbo.gal_states.state_code = dbo.states.sta_abbreviation INNER JOIN
                      dbo.state_licensure ON dbo.states.sta_key = dbo.state_licensure.stl_sta_key
ORDER BY dbo.gal_states.state_name

