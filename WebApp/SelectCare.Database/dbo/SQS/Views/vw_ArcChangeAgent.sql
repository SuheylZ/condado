
CREATE VIEW [dbo].[vw_ArcChangeAgent]
AS
SELECT     TOP (100) PERCENT h.ach_key, h.ach_entry, h.ach_account_key, h.ach_comment, h.ach_userid, h.ach_added_date, h.ach_entryType, h.ach_action_key, 
                      h.ach_cur_status_key, h.ach_cur_sub_status_key, h.ach_new_status_key, h.ach_new_sub_status_key, h.ach_delivered_to_arc, h.ach_talktime, h.ach_dnis, 
                      h.ach_contactId, h.ach_pv_key, h.ach_usr_csr, h.ach_usr_assigned, h.ach_usr_ta, h.ach_usr_ap, h.ach_usr_ob, h.ach_policy_status_type, 
                      h.ach_policy_status_key
FROM         dbo.account_history AS h INNER JOIN
                          (SELECT     MIN(ach_key) AS selected_ach_key
                            FROM          dbo.account_history
                            GROUP BY ach_account_key) AS tmp ON h.ach_key = tmp.selected_ach_key
WHERE     (h.ach_comment = 'User assigned') AND (h.ach_delivered_to_arc = 0) AND (h.ach_entryType = 2)


